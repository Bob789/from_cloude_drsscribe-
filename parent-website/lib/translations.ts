export function t(lang: LangCode, key: keyof Translations, vars?: Record<string, string | number>): string {
  const str = translations[lang]?.[key] ?? translations['en'][key] ?? key
  if (!vars) return str
  return Object.entries(vars).reduce((s, [k, v]) => s.replace(`{${k}}`, String(v)), str)
}

export function isRTL(lang: LangCode): boolean {
  return RTL_LANGS.includes(lang)
}
